gm.AddLogic("platform", {
	// sprite: "solidBoxFull",
		solid: true,
	// *** Our sprite is a bit big, so I'm shrinking the sprite down!
		xscale: 1/4,
		yscale: 1/4,
		bbox: undefined,
	Update: function() {
		var mario = gm.FindByLogic("Mario");

		if (this.vy <+ 0) {
				if (mario.CollideWith(this) == "bottom") {
					solid: false
				}
		}
		if (this.vy = 0) {
			solid: true
		}
	}
});













